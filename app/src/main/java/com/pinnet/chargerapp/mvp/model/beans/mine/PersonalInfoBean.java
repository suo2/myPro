package com.pinnet.chargerapp.mvp.model.beans.mine;

import java.io.Serializable;

/**
 * @author P00558
 * @date 2018/4/14
 */

public class PersonalInfoBean implements Serializable {
    /**
     * 用户Id
     */
    String userid;
    /**
     * 联系方式
     */
    String phoneNum;
    /**
     * 用户地址
     */
    String userAddr;
    /**
     * 用户昵称
     */
    String nickName;
    String userAvatar;
    /**
     * 用户头像更新时间
     */
    String userAvatarUpdateTIme;

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getUserAddr() {
        return userAddr;
    }

    public void setUserAddr(String userAddr) {
        this.userAddr = userAddr;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getUserAvatar() {
        return userAvatar;
    }

    public void setUserAvatar(String userAvatar) {
        this.userAvatar = userAvatar;
    }

    public String getUserAvatarUpdateTIme() {
        return userAvatarUpdateTIme;
    }

    public void setUserAvatarUpdateTIme(String userAvatarUpdateTIme) {
        this.userAvatarUpdateTIme = userAvatarUpdateTIme;
    }
}
